import java.io.Serializable;

public class Users {
    public String Date;
    public String Name;
    public String Email;
    public String UID;
    public String V1;
    public String V2;

    public Users(String date, String name, String email, String UID, String v1, String v2) {
        Date = date;
        Name = name;
        Email = email;
        this.UID = UID;
        V1 = v1;
        V2 = v2;
    }

    public String getDate() {
        return Date;
    }

    public void setDate(String date) {
        Date = date;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getEmail() {
        return Email;
    }

    public void setEmail(String email) {
        Email = email;
    }

    public String getUID() {
        return UID;
    }

    public void setUID(String UID) {
        this.UID = UID;
    }

    public String getV1() {
        return V1;
    }

    public void setV1(String v1) {
        V1 = v1;
    }

    public String getV2() {
        return V2;
    }

    public void setV2(String v2) {
        V2 = v2;
    }
}
